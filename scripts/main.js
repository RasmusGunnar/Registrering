function decodeBase64ToHtml(encoded) {
  const binary = atob(encoded);
  const bytes = Uint8Array.from(binary, char => char.charCodeAt(0));
  const decoder = new TextDecoder('utf-8');
  return decoder.decode(bytes);
}

